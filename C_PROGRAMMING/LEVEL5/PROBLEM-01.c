/*write a loop program to print 1 to 5 on one by one*/
#include <stdio.h>
int main()
{
    int i;
    for(i=1;i<=5;i++){//(intilize;condition;action)
           printf("%d\n",i); //for can be used do range type of problems,when we know to count eg: 1 to 1000 and check something
    }
    return 0;
}
